shoplist = ["apple", "mango", "carrot", "banana"]
name = 'Swaroop'

# Operation Index
print("Element 0:", shoplist[0])
print("Element 1:", shoplist[1])
print("Element 2:", shoplist[2])
print("Element 3:", shoplist[3])
print("Element -1:", shoplist[-1])
print("Element -2:", shoplist[-2])
print("Symbol 0:", name[0])

# Cupure de la list
print("First three elements of the list:", shoplist[:3])
print("Three elements from the middle of the list:", shoplist[1:4])
print("Last three elements of the list:", shoplist[-3:])
print("Elements up to the middle of the list:", shoplist[:])

# Cupure de la STR
print("First three characters of the name:", name[:3])
print("Three characters from the middle of the name:", name[1:4])
print("Last three characters of the name:", name[-3:])
print("The whole name:", name)

